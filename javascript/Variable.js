// variable declaration and addition
let a =5;
let b =10;

console.log(a + b);

//var
var c =15;
var d =20;

console.log(c + d);

//const
const e =25;
const f =30;

console.log(e + f);

//string concatenation
let str1 = "Hello, ";
let str2 = "World!";

console.log(str1 + str2);

//mixed addition
let num = 100;
let str3 = " is a number.";

console.log(num + str3);        

// Output:
// 15
// 35
// 55
// Hello, World!
// 100 is a number.
