// Operator (+,-,*,/,%,==,!=,===,!==,<,>,<=,>=,&&,||,!,++,--)

let a = 10;
let b = 5;

// Addition
console.log("Addition:", a + b); // 15

// Subtraction
console.log("Subtraction:", a - b); // 5

// Multiplication
console.log("Multiplication:", a * b); // 50

// Division
console.log("Division:", a / b); // 2

// Modulus
console.log("Modulus:", a % b); // 0

// Equality
console.log("Equality (==):", a == b); // false

// Inequality
console.log("Inequality (!=):", a != b); // true

// Strict Equality
console.log("Strict Equality (===):", a === b); // false

// Strict Inequality
console.log("Strict Inequality (!==):", a !== b); // true

// Greater than
console.log("Greater than (>):", a > b); // true

// Less than
console.log("Less than (<):", a < b); // false

// Greater than or equal to
console.log("Greater than or equal to (>=):", a >= b); // true

// Less than or equal to
console.log("Less than or equal to (<=):", a <= b); // false

// Logical AND
console.log("Logical AND (&&):", a > 5 && b < 10); // true

// Logical OR
console.log("Logical OR (||):", a > 15 || b < 10); // true

// Logical NOT
console.log("Logical NOT (!):", !(a > b)); // false

// Increment
console.log("Increment (a++):", a++); // 10
console.log("Value of a after increment:", a); // 11

// Decrement
console.log("Decrement (b--):", b--); // 5
console.log("Value of b after decrement:", b            
); // 4

